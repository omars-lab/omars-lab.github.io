#!/usr/bin/env node

/**
 * generate-component-usage.js — build the "used in" index for the showcase docs.
 *
 * Each showcase under docs/legend/components/* demonstrates one blog ability (a <Card>, a mermaid
 * diagram, a code embed). This scans every PUBLISHED post/doc for that technique and emits, per
 * showcase, the list of pages that actually use it — so a showcase's "Used in" list is generated
 * from reality and can never go stale (the same drift-proof model as the kanban/changelog data).
 *
 * HOW a showcase declares what to look for: a `usage_pattern` in its frontmatter — a literal
 * substring the technique leaves in a post's body (e.g. `usage_pattern: "<Card"` or
 * `usage_pattern: "```mermaid"`). A showcase with no `usage_pattern` is skipped (it shows nothing).
 * Multiple patterns may be given as a YAML list; a post matches if ANY pattern is present.
 *
 * OUTPUT (gitignored, regenerated by `npm run generate-assets`; NEVER hand-edit — the
 * block-generated-edits hook refuses writes to it):
 *   src/components/UsedIn/component-usage.json
 *
 * SHAPE: { "<showcase-slug>": [ {title, permalink} ] }  (the showcase docs themselves are excluded
 * from the scan so a showcase never lists itself, and drafts are excluded so a card never links to
 * a page that 404s in prod).
 */

const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');

const ROOT = path.join(__dirname, '..');
const SHOWCASES_DIR = path.join(ROOT, 'docs', 'legend', 'components');
const outputFile = path.join(ROOT, 'src', 'components', 'UsedIn', 'component-usage.json');

// Content roots to scan for usage, with their permalink base (mirrors the other generators).
const DOCS_INSTANCES = [
  {dir: 'docs/craft', base: '/craft'},
  {dir: 'docs/journey', base: '/journey'},
  {dir: 'docs/legend', base: '/legend'},
];
const BLOG_INSTANCES = [
  {dir: 'blog', base: '/initiatives'},
  {dir: 'designs', base: '/designs'},
  {dir: 'thoughts', base: '/thoughts'},
  {dir: 'mindset', base: '/mindset'},
  {dir: 'questions', base: '/questions'},
];

function walk(absDir, out = []) {
  if (!fs.existsSync(absDir)) return out;
  for (const e of fs.readdirSync(absDir, {withFileTypes: true})) {
    if (e.name === 'node_modules' || e.name.startsWith('.')) continue;
    const fp = path.join(absDir, e.name);
    if (e.isDirectory()) walk(fp, out);
    else if (/\.mdx?$/.test(e.name) && !e.name.startsWith('_')) out.push(fp);
  }
  return out;
}

function safeRead(file) {
  try {
    const raw = fs.readFileSync(file, 'utf-8');
    const {data, content} = matter(raw);
    return {data: data || {}, content: content || ''};
  } catch {
    return null;
  }
}

// Permalink for a content file (docs: absolute slug or path-derived; blog: slug or de-dated name).
function permalinkFor(file, data, base, isBlog) {
  if (typeof data.slug === 'string' && data.slug.startsWith('/')) {
    return data.slug === '/' ? base : base + data.slug;
  }
  if (isBlog) {
    const fileSlug = path.basename(file).replace(/\.(md|mdx)$/, '').replace(/^\d{4}-\d{2}-\d{2}-/, '');
    return `${base}/${(data.slug || fileSlug).toString().replace(/^\//, '')}`;
  }
  // docs path-derived
  const instDir = base === '/craft' ? 'docs/craft' : base === '/journey' ? 'docs/journey' : 'docs/legend';
  const rel = path.relative(path.join(ROOT, instDir), file).replace(/\.mdx?$/, '');
  const segs = rel.split(path.sep).filter((s) => !/^README$|^index$/i.test(s));
  return [base, ...segs].filter(Boolean).join('/');
}

function build() {
  // 1. Collect the showcases + their usage patterns.
  const showcases = []; // {slug, patterns: [str], file}
  for (const f of walk(SHOWCASES_DIR)) {
    const r = safeRead(f);
    if (!r) continue;
    const slug = typeof r.data.slug === 'string' ? r.data.slug : null;
    if (!slug) continue;
    const up = r.data.usage_pattern;
    const patterns = Array.isArray(up) ? up.map(String) : up ? [String(up)] : [];
    if (!patterns.length) continue;
    showcases.push({slug, patterns, file: f});
  }

  // 2. Scan every published post/doc body for each showcase's pattern(s).
  const showcaseFiles = new Set(showcases.map((s) => s.file));
  const usage = Object.fromEntries(showcases.map((s) => [s.slug, []]));

  const scan = (instances, isBlog) => {
    for (const {dir, base} of instances) {
      for (const f of walk(path.join(ROOT, dir))) {
        if (showcaseFiles.has(f)) continue; // a showcase never lists itself
        const r = safeRead(f);
        if (!r || r.data.draft === true) continue; // skip drafts (would 404 in prod)
        const body = r.content;
        for (const s of showcases) {
          if (s.patterns.some((p) => body.includes(p))) {
            usage[s.slug].push({
              title: (r.data.title || path.basename(f)).toString().replace(/^['"]|['"]$/g, ''),
              permalink: permalinkFor(f, r.data, base, isBlog),
            });
          }
        }
      }
    }
  };
  scan(DOCS_INSTANCES, false);
  scan(BLOG_INSTANCES, true);

  // Sort each list by title for stable output.
  for (const list of Object.values(usage)) {
    list.sort((a, b) => a.title.localeCompare(b.title));
  }
  return usage;
}

const usage = build();
const outDir = path.dirname(outputFile);
if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, {recursive: true});
fs.writeFileSync(outputFile, JSON.stringify(usage, null, 2), 'utf-8');

const withUsage = Object.values(usage).filter((l) => l.length).length;
console.log(
  `Generated component usage: ${Object.keys(usage).length} showcases (${withUsage} with usage) -> ${path.relative(ROOT, outputFile)}`,
);
