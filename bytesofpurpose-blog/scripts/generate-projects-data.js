#!/usr/bin/env node

/**
 * generate-projects-data.js — build the ProjectsCatalog data from blog-post frontmatter.
 *
 * Mirrors generate-kanban-data.js: scan the /initiatives blog posts, read each post's
 * frontmatter, and emit a JSON the <ProjectsCatalog> component consumes. An entry is
 * generated PER POST that carries a `project_area:` field (never hand-authored), so the
 * durable Projects hub can never drift from the initiative posts it indexes — moving a
 * project between areas = changing that post's `project_area` frontmatter.
 *
 * A "project" here = a dated project LOG that lives on /initiatives (temporal, acted-on
 * work). The hub at /craft/software-development/projects is the DURABLE index that groups
 * these by area and links down to each post.
 *
 * OUTPUT (gitignored, regenerated by `npm run generate-assets`; NEVER hand-edit — the
 * block-generated-edits hook refuses writes to it):
 *   src/components/ProjectsCatalog/projects-data.json
 *
 * SHAPE: { "<area>": [ {slug, title, description, permalink, date, draft, tags} ] }
 *   area ∈ AREAS below (frontend | backend | script | other). A post's `project_area`
 *   that is not a known area lands under "other".
 *
 * DRAFTS: unlike the kanban board (which hard-excludes drafts because a card links to a
 * prod URL), the Projects hub is a /craft doc that renders in BOTH dev and prod. We keep
 * draft entries but MARK them `draft: true` so the component can render them as
 * "in progress / not yet published" WITHOUT linking (a link to a draft 404s in prod). The
 * component must not emit an <a> to a draft permalink.
 */

const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');

const ROOT = path.join(__dirname, '..');
const BLOG_DIR = path.join(ROOT, 'blog');
const BLOG_BASE = '/initiatives';
const outputFile = path.join(ROOT, 'src', 'components', 'ProjectsCatalog', 'projects-data.json');

// Ordered areas. Anything else falls into "other".
const AREAS = ['frontend', 'backend', 'script', 'other'];

// Tags that are structural noise, not descriptive theme tags (kept small on purpose).
const NOISE_TAGS = new Set(['development', 'project', 'projects']);

function listPosts(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir, {withFileTypes: true})
    .filter((e) => e.isFile() && /\.mdx?$/.test(e.name) && !e.name.startsWith('_'))
    .map((e) => e.name);
}

function isoDate(d) {
  if (!d) return '';
  try {
    return new Date(d).toISOString().slice(0, 10);
  } catch {
    return '';
  }
}

function themeTags(tags) {
  if (!Array.isArray(tags)) return [];
  return tags.map((t) => t.toString()).filter((t) => t && !NOISE_TAGS.has(t.toLowerCase()));
}

function build() {
  const areas = Object.fromEntries(AREAS.map((a) => [a, []]));

  for (const name of listPosts(BLOG_DIR)) {
    const raw = fs.readFileSync(path.join(BLOG_DIR, name), 'utf-8');
    let fm;
    try {
      fm = matter(raw).data || {};
    } catch {
      continue; // unparseable frontmatter — other validators flag it
    }
    if (!fm.project_area) continue; // opt-in: only posts that declare an area

    const fileSlug = name.replace(/\.(md|mdx)$/, '').replace(/^\d{4}-\d{2}-\d{2}-/, '');
    const slug = (fm.slug || fileSlug).toString().replace(/^\//, '');
    const area = AREAS.includes(fm.project_area) ? fm.project_area : 'other';

    areas[area].push({
      slug,
      title: (fm.title || fileSlug).toString().replace(/^['"]|['"]$/g, ''),
      description: (fm.description || fm.summary || '').toString(),
      permalink: `${BLOG_BASE}/${slug}`,
      date: isoDate(fm.date),
      draft: fm.draft === true,
      tags: themeTags(fm.tags),
    });
  }

  // Newest first within each area (matches the changelog/ideas/kanban convention).
  for (const list of Object.values(areas)) {
    list.sort((a, b) => (b.date || '').localeCompare(a.date || ''));
  }
  return areas;
}

const data = build();
const outDir = path.dirname(outputFile);
if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, {recursive: true});
fs.writeFileSync(outputFile, JSON.stringify(data, null, 2), 'utf-8');

const counts = AREAS.map((a) => `${a}:${data[a].length}`).join(' ');
console.log(`Generated projects data (${counts}) -> ${outputFile}`);
