#!/usr/bin/env node

/**
 * generate-repos-data.js — build the RepoCatalog data from the hand-authored source.
 *
 * Unlike the post-frontmatter generators (kanban/ideas/projects), a "repo of interest" is
 * NOT a page — it is a catalog ENTRY ({name, url, blurb, tags}). So the SOURCE OF TRUTH is
 * a hand-authored file (src/data/repos-of-interest.json), and this generator's job is to
 * VALIDATE + NORMALIZE it into the shape the <RepoCatalog> component renders (grouped by
 * tag), so the component never imports the hand-edited file directly and bad data fails
 * loudly at generate time instead of rendering broken.
 *
 * OUTPUT (gitignored, regenerated by `npm run generate-assets`; NEVER hand-edit — the
 * block-generated-edits hook refuses writes to it):
 *   src/components/RepoCatalog/repos-data.json
 *
 * SHAPE: { tags: {tag: gloss}, groups: [ {tag, gloss, repos: [{name,url,blurb,tags}]} ],
 *          all: [ {name,url,blurb,tags} ] }
 *   A repo appears under EVERY tag it carries (a repo can be both "tool" and "testing").
 *   `all` is the de-duped flat list (for a possible "all repos" view / count).
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const SOURCE = path.join(ROOT, 'src', 'data', 'repos-of-interest.json');
const OUTPUT = path.join(ROOT, 'src', 'components', 'RepoCatalog', 'repos-data.json');

function fail(msg) {
  console.error(`generate-repos-data: ${msg}`);
  process.exit(1);
}

if (!fs.existsSync(SOURCE)) fail(`source not found: ${SOURCE}`);

let src;
try {
  src = JSON.parse(fs.readFileSync(SOURCE, 'utf-8'));
} catch (e) {
  fail(`source is not valid JSON: ${e.message}`);
}

const tagGloss = src.__tags__ || {};
const repos = Array.isArray(src.repos) ? src.repos : fail('source.repos must be an array');

// The blurbs + glosses are USER-FACING (they render in the catalog), so hold them to the
// same no-em-dash voice rule the repo enforces on .md/.mdx content (the em-dash-voice hook
// does not scan .json, so guard it here — fail closed rather than silently ship AI voice).
function assertNoEmDash(label, text) {
  if (/—/.test(text) || /(^|[^-])--([^-]|$)/.test(text)) {
    fail(`${label} contains an em-dash / "--" (reads as AI voice): "${text}". Rephrase with a comma, colon, or two sentences.`);
  }
}
Object.entries(tagGloss).forEach(([tag, gloss]) => assertNoEmDash(`tag gloss '${tag}'`, gloss));

// Validate + normalize each entry.
const seenUrls = new Set();
const clean = [];
repos.forEach((r, i) => {
  const where = `repos[${i}]${r && r.name ? ` (${r.name})` : ''}`;
  if (!r || typeof r !== 'object') fail(`${where} is not an object`);
  if (!r.name || typeof r.name !== 'string') fail(`${where} missing string 'name'`);
  if (!r.url || !/^https?:\/\//.test(r.url)) fail(`${where} missing valid 'url' (http/https)`);
  if (!r.blurb || typeof r.blurb !== 'string') fail(`${where} missing string 'blurb'`);
  assertNoEmDash(`${where} blurb`, r.blurb);
  if (!Array.isArray(r.tags) || r.tags.length === 0) fail(`${where} must have a non-empty 'tags' array`);
  if (seenUrls.has(r.url)) fail(`${where} duplicate url ${r.url}`);
  seenUrls.add(r.url);
  clean.push({
    name: r.name,
    url: r.url,
    blurb: r.blurb,
    tags: r.tags.map((t) => t.toString()),
  });
});

// Group by tag, in the order tags first appear in __tags__ (so the gloss order drives the
// section order), then any extra tags used but not glossed, appended alphabetically.
const glossedOrder = Object.keys(tagGloss);
const usedTags = new Set(clean.flatMap((r) => r.tags));
const extraTags = [...usedTags].filter((t) => !glossedOrder.includes(t)).sort();
const orderedTags = [...glossedOrder.filter((t) => usedTags.has(t)), ...extraTags];

const groups = orderedTags.map((tag) => ({
  tag,
  gloss: tagGloss[tag] || '',
  repos: clean.filter((r) => r.tags.includes(tag)),
}));

const data = {tags: tagGloss, groups, all: clean};

const outDir = path.dirname(OUTPUT);
if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, {recursive: true});
fs.writeFileSync(OUTPUT, JSON.stringify(data, null, 2), 'utf-8');
console.log(`Generated repos data (${clean.length} repos, ${groups.length} tag groups) -> ${OUTPUT}`);
